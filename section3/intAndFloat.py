"""
int and float

int -> Integer
Represent any integer as 1, 2, 3
Positive or negative. int without sign is positive.

float -> decimal
Represent any decimal as 1.1, 1.2, 1.3
Positive or negative. float without sign is positive.

The function 'type' will show the type that Python has inferred to the value
"""
# int
print(11)
print(-11)
print(0)

# float
print(1.1)
print(-1.1)
print(0.0)

# type
print(type(11))
print(type(1.1))
